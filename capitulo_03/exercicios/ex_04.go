package main

import "fmt"

func main() {
	fmt.Println("Ex 04: Qual é a diferença entre *var* e *const*")

	fmt.Println("\nResposta:")
	fmt.Println("- var: utilizado para criar variávies, isto é, valores que podem sofrer alterações ao longo da execução do programa.")
	fmt.Println("- const: utilizado para criar constantes, onde uma vez atribuído, não pode sofrer alterações.")
}
